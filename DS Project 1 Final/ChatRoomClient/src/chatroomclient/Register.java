    public void checkRegister(boolean check)
    {
        if(check)
        {
            JOptionPane.showMessageDialog(this, "Registration is complete, you can now Log in!");
        }
        else
            JOptionPane.showMessageDialog(this, "Username already exists, try another one");
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!checkFirst())
        {
            JOptionPane.showMessageDialog(this, "Passwords dont match, try again!");
            return;
        }
        try
        {
        if(app.client==null)
            {
                //if client is null create new
                app.client=new Client
                    (InetAddress.getByName(app.ipServer),app.port,
                    txtUserName.getText());
                app.client.setApp(app);
                app.client.start();
                app.name=txtUserName.getText();
            
            }
        String message=app.name+MessageChat.dotMsg+MessageChat.Register+MessageChat.dotMsg+
                    "0"+MessageChat.dotMsg+txtUserName.getText()+MessageChat.dotMsg+txtPass.getText();
        app.client.sendMessage(message);
        }
        catch(Exception ae){
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

